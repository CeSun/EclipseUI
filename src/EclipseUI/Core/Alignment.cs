namespace EclipseUI.Core;

/// <summary>
/// 水平对齐方式
/// </summary>
public enum HorizontalAlignment
{
    /// <summary>
    /// 左对齐
    /// </summary>
    Left,
    
    /// <summary>
    /// 居中对齐
    /// </summary>
    Center,
    
    /// <summary>
    /// 右对齐
    /// </summary>
    Right,
    
    /// <summary>
    /// 拉伸填满（默认）
    /// </summary>
    Stretch
}

/// <summary>
/// 垂直对齐方式
/// </summary>
public enum VerticalAlignment
{
    /// <summary>
    /// 顶部对齐
    /// </summary>
    Top,
    
    /// <summary>
    /// 垂直居中
    /// </summary>
    Center,
    
    /// <summary>
    /// 底部对齐
    /// </summary>
    Bottom,
    
    /// <summary>
    /// 拉伸填满（默认）
    /// </summary>
    Stretch
}
